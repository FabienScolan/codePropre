package ex1;

public class calcul {

	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return La somme de a et b
	 */
	public int getSomme(int a, int b){
		return a+b;
	}
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return la soustraction de b à a
	 */
	public int getSubstract(int a, int b){
		return a-b;
	}
}
